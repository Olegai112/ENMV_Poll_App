class SmartphoneWithCamera(Smartphone):
    """–î–æ—á–µ—Ä–Ω–∏–π –∫–ª–∞—Å—Å - —Å–º–∞—Ä—Ç—Ñ–æ–Ω —Å —É–ª—É—á—à–µ–Ω–Ω–æ–π –∫–∞–º–µ—Ä–æ–π"""

