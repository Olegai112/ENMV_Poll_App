class Smartphone:
    # –ê—Ç—Ä–∏–±—É—Ç –∫–ª–∞—Å—Å–∞ (–æ–±—â–∏–π –¥–ª—è –≤—Å–µ—Ö —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤)
    year_of_creation = 2024
    total_phones = 0  # –°—á–µ—Ç—á–∏–∫ —Å–æ–∑–¥–∞–Ω–Ω—ã—Ö —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤

    # –ö–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä (–∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –æ–±—ä–µ–∫—Ç–∞)
    def __init__(self, brand, model, battery_level=100):
        # –ü—É–±–ª–∏—á–Ω—ã–µ –∞—Ç—Ä–∏–±—É—Ç—ã
        self.brand = brand
        self.model = model
        self.color = "—á–µ—Ä–Ω—ã–π"  # –ó–Ω–∞—á–µ–Ω–∏–µ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é

        # "–ü—Ä–∏–≤–∞—Ç–Ω—ã–µ" –∞—Ç—Ä–∏–±—É—Ç—ã (–ø–æ —Å–æ–≥–ª–∞—à–µ–Ω–∏—é)
        self._battery = battery_level  # –ó–∞—â–∏—â–µ–Ω–Ω—ã–π (protected)
        self.__password = "1234"  # –ü—Ä–∏–≤–∞—Ç–Ω—ã–π (private) - name mangling

        # –°—á–µ—Ç—á–∏–∫ —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ —É–≤–µ–ª–∏—á–∏–≤–∞–µ—Ç—Å—è –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –Ω–æ–≤–æ–≥–æ
        Smartphone.total_phones += 1

    # –û–±—ã—á–Ω—ã–π –º–µ—Ç–æ–¥
    def show_info(self):
        """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ —Ç–µ–ª–µ—Ñ–æ–Ω–µ"""
        return f"{self.brand} {self.model}, —Ü–≤–µ—Ç: {self.color}, –∑–∞—Ä—è–¥: {self._battery}%"

    # –ú–µ—Ç–æ–¥ –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ –¥–æ—Å—Ç—É–ø–∞ –∫ –ø—Ä–∏–≤–∞—Ç–Ω–æ–º—É –∞—Ç—Ä–∏–±—É—Ç—É
    def check_password(self, password):
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç –ø–∞—Ä–æ–ª—å"""
        return password == self.__password

    # –ú–µ—Ç–æ–¥ –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è –æ–±—ä–µ–∫—Ç–∞
    def charge(self, minutes):
        """–ó–∞—Ä—è–∂–∞–µ—Ç —Ç–µ–ª–µ—Ñ–æ–Ω"""
        self._battery += minutes * 2
        if self._battery > 100:
            self._battery = 100
        print(f"–¢–µ–ª–µ—Ñ–æ–Ω –∑–∞—Ä—è–∂–µ–Ω. –¢–µ–∫—É—â–∏–π –∑–∞—Ä—è–¥: {self._battery}%")

    # –ú–∞–≥–∏—á–µ—Å–∫–∏–π –º–µ—Ç–æ–¥ __str__ (–¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π)
    def __str__(self):
        return f"üì± {self.brand} {self.model}"

    # –ú–∞–≥–∏—á–µ—Å–∫–∏–π –º–µ—Ç–æ–¥ __repr__ (–¥–ª—è —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤)
    def __repr__(self):
        return f"Smartphone('{self.brand}', '{self.model}')"

    # –°—Ç–∞—Ç–∏—á–µ—Å–∫–∏–π –º–µ—Ç–æ–¥ (–Ω–µ –∑–∞–≤–∏—Å–∏—Ç –æ—Ç –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ –æ–±—ä–µ–∫—Ç–∞)
    @staticmethod
    def is_fully_charged(battery_level):
        """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –ø–æ–ª–Ω–æ—Å—Ç—å—é –ª–∏ –∑–∞—Ä—è–∂–µ–Ω —Ç–µ–ª–µ—Ñ–æ–Ω"""
        return battery_level >= 100

    # –ú–µ—Ç–æ–¥ –∫–ª–∞—Å—Å–∞ (—Ä–∞–±–æ—Ç–∞–µ—Ç —Å –∞—Ç—Ä–∏–±—É—Ç–∞–º–∏ –∫–ª–∞—Å—Å–∞)
    @classmethod
    def create_iphone(cls, model):
        """–°–æ–∑–¥–∞–µ—Ç iPhone —Å –∑–∞–¥–∞–Ω–Ω–æ–π –º–æ–¥–µ–ª—å—é"""
        return cls("Apple", model)


# ========== –î–ï–ú–û–ù–°–¢–†–ê–¶–ò–Ø –†–ê–ë–û–¢–´ ==========

